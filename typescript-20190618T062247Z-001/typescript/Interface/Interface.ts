// Interface is a structure that defines the contract in your application. 
// It defines the syntax for classes to follow.
//  Classes that are derived from an interface must follow 
//  the structure provided by their interface.

// The TypeScript compiler does not convert interface to JavaScript.
//  It uses interface for type checking.
//   This is also known as "duck typing" or "structural subtyping".

// An interface is defined with the keyword interface
//  and it can include properties and method declarations using a function or an arrow function. 